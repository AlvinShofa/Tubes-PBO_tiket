    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your   handling code here:
        
        try{
            Connection c=koneksi.getkoneksi();
            
            sql = "SELECT * FROM pesanan WHERE customer_emailcust = '" + email +"';";
            
            st= c.createStatement();
            rs= st.executeQuery(sql);
            
            model.getDataVector().removeAllElements();
            tabel_tiket.setModel(model);
            
            while (rs.next()){
                Object[] data={
                    rs.getInt("id_pesanan"),
                    rs.getString("wahana_nama_wahana"),
                    rs.getString("jenis_tiket"),
                    rs.getString("tanggal_pemesanan")
                };
                model.addRow(data);
            }
            
                tabel_tiket.getColumnModel().getColumn(0).setPreferredWidth(200); // Kolom ID Pemesanan
                tabel_tiket.getColumnModel().getColumn(1).setPreferredWidth(200); // Kolom Jenis Wahana
                tabel_tiket.getColumnModel().getColumn(2).setPreferredWidth(200); // Kolom Tipe Tiket
                tabel_tiket.getColumnModel().getColumn(3).setPreferredWidth(200); // Kolom Tanggal Pemesanan
                
                tabel_tiket.setRowHeight(60);
                
                 tabel_tiket.getTableHeader().setFont(new Font("Times New Roman", Font.PLAIN, 36));

            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

