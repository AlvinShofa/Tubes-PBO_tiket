    private void btn_gunaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gunaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabel_tiket.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih tiket terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        int idPesanan = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());

        Connection c = koneksi.getkoneksi();

        try {
            String updateArsip = "UPDATE arsip SET status_tiket = 'Digunakan' WHERE id_pesanan = " + idPesanan+";";
            String deletePesanan = "DELETE FROM pesanan WHERE id_pesanan = " + idPesanan+";";

            st.executeUpdate(updateArsip);
            st.executeUpdate(deletePesanan);

            model.removeRow(selectedRow);

            JOptionPane.showMessageDialog(this, "Tiket berhasil digunakan.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menghapus tiket: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btn_gunaActionPerformed

