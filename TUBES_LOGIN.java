    private void btn_daftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_daftarActionPerformed
        // TODO add your handling code here:
        Nama = txt_nama.getText();
        Email = txt_email.getText();
        Password = String.valueOf(pwd.getPassword());
        
        try {
            Connection c = koneksi.getkoneksi();
            st = c.createStatement();
            String checkSql = "SELECT emailcust FROM customer WHERE emailcust = '" + Email + "';";
            rscust = st.executeQuery(checkSql);

            if (rscust.next()) {
                JOptionPane.showMessageDialog(this, "Email sudah terdaftar sebagai Customer", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                String insertSql = "INSERT INTO customer VALUES ('" + Email + "', '" + Nama + "', '" + Password + "');";
                st.executeUpdate(insertSql);
                JOptionPane.showMessageDialog(this, "Pendaftaran berhasil, silakan login");
                txt_nama.setText("");
                txt_email.setText("");
                pwd.setText("");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_daftarActionPerformed

