    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        Nama = txt_nama.getText();
        Email = txt_email.getText();
        Password = String.valueOf(pwd.getPassword());
        
        try {
            Connection c = koneksi.getkoneksi();
            st = c.createStatement();
            sql = "SELECT * FROM customer WHERE emailcust = '" + Email + "' AND pwd_cust = '" + Password + "' and namacust = '"+Nama+"';";
            rscust = st.executeQuery(sql);

            if (rscust.next()) {
                JOptionPane.showMessageDialog(this, "Login berhasil sebagai Customer");
                new Pemasanan_Tiket(Nama, Email).setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Email atau Password Customer salah", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
        
    }//GEN-LAST:event_btn_loginActionPerformed

